/*16.	Write a program to assign a value 3 to "int" type variables a, b and c using multiple assignment statement. Calculate the product of these numbers.*/
#include <iostream>
using namespace std;

int main() {
    int a = 3, b = 3, c = 3;
    int product = a * b * c;

    cout << "The product of " << a << ", " << b << " and " << c << " is: " << product << endl;

    return 0;
}



